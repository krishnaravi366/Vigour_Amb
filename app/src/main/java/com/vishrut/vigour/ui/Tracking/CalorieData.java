package com.vishrut.vigour.ui.Tracking;


public class CalorieData {
    public String gotDistance;
    public String gotTime;
    public String gotCalorie;

    public CalorieData(String gotDistance, String gotTime, String gotCalorie) {

    }

    public String getGotDistance() {
        return gotDistance;
    }

    public void setGotDistance(String gotDistance) {
        this.gotDistance = gotDistance;
    }

    public String getGotTime() {
        return gotTime;
    }

    public void setGotTime(String gotTime) {
        this.gotTime = gotTime;
    }

    public String getGotCalorie() {
        return gotCalorie;
    }

    public void setGotCalorie(String gotCalorie) {
        this.gotCalorie = gotCalorie;
    }

}
